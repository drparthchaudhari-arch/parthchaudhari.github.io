// Get random words for a level
export const getRandomWords = (count: number, difficulty: number): string[] => {
  const words: string[] = [];
  const usedWords = new Set<string>();
  
  // Mix of normal and vet words based on difficulty
  // Higher difficulty = more vet words
  const vetWordRatio = Math.min(0.3 + (difficulty * 0.02), 0.8);
  const vetWordCount = Math.floor(count * vetWordRatio);
  const normalWordCount = count - vetWordCount;
  
  // Add vet words
  let attempts = 0;
  while (words.filter(w => vetWords.includes(w)).length < vetWordCount && attempts < 100) {
    const word = vetWords[Math.floor(Math.random() * vetWords.length)];
    if (!usedWords.has(word) && word.length >= 3 && word.length <= 10) {
      words.push(word);
      usedWords.add(word);
    }
    attempts++;
  }
  
  // Add normal words
  attempts = 0;
  while (words.filter(w => normalWords.includes(w)).length < normalWordCount && attempts < 100) {
    const word = normalWords[Math.floor(Math.random() * normalWords.length)];
    if (!usedWords.has(word) && word.length >= 3 && word.length <= 10) {
      words.push(word);
      usedWords.add(word);
    }
    attempts++;
  }
  
  return words;
};

// Get theme for level
export const getTheme = (difficulty: number): string => {
  const useVetTheme = Math.random() < (0.3 + difficulty * 0.01);
  if (useVetTheme) {
    return vetThemes[Math.floor(Math.random() * vetThemes.length)];
  }
  return normalThemes[Math.floor(Math.random() * normalThemes.length)];
};
